function setLanguage(lang) {
  console.log('🌍 Setting language to:', lang);
  localStorage.setItem('dashboard_lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  applyTranslations();
  
  // Update toggle button text if exists
  const langBtn = document.getElementById('toggleLanguage');
  if (langBtn) {
    const nextLangText = translations[lang]['switch-lang'];
    console.log('   Next lang toggle text will be:', nextLangText);
    langBtn.innerHTML = `<span>${nextLangText}</span>`;
  }
}

function applyTranslations() {
  const lang = localStorage.getItem('dashboard_lang') || 'en';
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === 'INPUT' && el.placeholder) {
        el.placeholder = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
}

// Make them global
window.setLanguage = setLanguage;
window.applyTranslations = applyTranslations;

// Global initialization
window.initI18n = () => {
  const savedLang = localStorage.getItem('dashboard_lang') || 'en';
  setLanguage(savedLang);
};
